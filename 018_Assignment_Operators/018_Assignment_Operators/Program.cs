using System;

namespace _018_Assignment_Operators
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 40;
            x += 2; //equivalent to x = x + 2
            Console.WriteLine(x);
            int y = 48;
            y -= 6; //equivalent to y = y - 6
            Console.WriteLine(y);
        }
    }
}
